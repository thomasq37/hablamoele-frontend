document.addEventListener("DOMContentLoaded", () => {
    // V√©rifier si une langue est stock√©e dans le localStorage
    const storedLanguage = localStorage.getItem("language") || 'es'; // Langue par d√©faut si aucune n'est stock√©e
    changeLanguage(storedLanguage); // Changer la langue

    const languageSwitcher = document.getElementById("languageSwitcher");
    languageSwitcher.value = storedLanguage; // Mettre √† jour le s√©lecteur avec la langue actuelle

    languageSwitcher.addEventListener("change", (event) => {
        const selectedLanguage = event.target.value;
        changeLanguage(selectedLanguage);
        // Enregistrer la langue choisie dans le localStorage
        localStorage.setItem("language", selectedLanguage);
    });
});

